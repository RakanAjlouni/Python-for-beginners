#Prompt the user to enter their age

age = int(input("Enter your age: ")) #Here, it asks the user to input their age.
#The input function takes whatever the user types in as a string.
#So we use int to convert it to an integer which is a whole number.

#Multiply the age by 2
result = age * 2 #We Multiply the age entered by the user by 2 using the * operator.
#And story the result in the variable result.

#Display the result
print("Twice your age is:", result) #Finally, we print out the result to the user.
#The print function is used to display information to the user.